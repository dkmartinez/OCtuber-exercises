package CoreJavaProgramming;

public class Static_Keyword {
	/**
	 * 
	 * Static keyword
	 * Static members belong to class(type) and not to objects 
	 * 
	 */
	
	String name = "ABD";
	byte age = 6;
	String grade = "1st";
	
	public void displayDetails() {
		System.out.println("Student " + name + " has " + age + " and his grade is " + grade);
	}
	

}
